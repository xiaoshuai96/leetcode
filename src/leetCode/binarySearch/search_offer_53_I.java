package leetCode.binarySearch;
/**
 * 统计一个数字在排序数组中出现的次数。
 *
 * 示例 1:
 * 输入: nums = [5,7,7,8,8,10], target = 8
 * 输出: 2
 *
 * 示例 2:
 * 输入: nums = [5,7,7,8,8,10], target = 6
 * 输出: 0
 *
 * [-99999,-99998,-9999,-999,-99,-9,-1]
 * 0
 * 限制：
 *
 * 0 <= 数组长度 <= 50000
 */
public class search_offer_53_I {
    public static void main(String[] args) {
        int i = new search_offer_53_I().search(new int[]{}, 0);
        System.out.println(i);
    }

    /**
     * 执行用时：0 ms, 在所有 Java 提交中击败了100.00% 的用户
     * 内存消耗：41.4 MB, 在所有 Java 提交中击败了77.56% 的用户
     * @param nums
     * @param target
     * @return
     */
    public int search(int[] nums, int target) {
        // 搜索右边界 right
        int i = 0, j = nums.length - 1;
        while(i <= j) {
            int m = (i + j) / 2;
            if(nums[m] <= target) i = m + 1;
            else j = m - 1;
        }
        int right = i;
        // 若数组中无 target ，则提前返回
        if(j >= 0 && nums[j] != target) return 0;
        // 搜索左边界 right
        i = 0; j = nums.length - 1;
        while(i <= j) {
            int m = (i + j) / 2;
            if(nums[m] < target) i = m + 1;
            else j = m - 1;
        }
        int left = j;
        return right - left - 1;
    }
}
